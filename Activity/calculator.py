operator = input("Enter the operator (+,-,*,/): ")
inp1 = int(input("Enter the first number: "))
inp2 = int(input("Enter the second number: "))

if operator == "+":
    print(inp1 + inp2)
elif operator == "-":
    print(inp1 - inp2)
elif operator == "*":
    print(inp1 * inp2)
elif operator == "/":
    print(inp1 / inp2)
else:
    print("Invalid operator :< Please rerun the code")

